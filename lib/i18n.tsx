function detectLocale(): Locale {
  if (typeof window !== "undefined") {
    const savedLocale = localStorage.getItem("preferred_locale") as Locale | null;
    if (savedLocale === "fr-CA" || savedLocale === "en-CA" || savedLocale === "es-MX") {
      return savedLocale;
    }
  }

  if (typeof navigator === "undefined") return "fr-CA"

  const exactMap: Record<string, Locale> = {
    "fr-ca": "fr-CA",
    "en-ca": "en-CA",
    "es-mx": "es-MX",
  }
  const prefixMap: Record<string, Locale> = {
    fr: "fr-CA",
    en: "en-CA",
    es: "es-MX",
  }

  // Check all preferred languages in order (navigator.languages)
  const langs = navigator.languages?.length
    ? navigator.languages
    : [navigator.language || (navigator as { userLanguage?: string }).userLanguage || ""]

  for (const lang of langs) {
    const lower = lang.toLowerCase()
    // Exact match first (e.g. fr-CA, es-MX)
    if (exactMap[lower]) return exactMap[lower]
    // Prefix match (e.g. fr, en, es, fr-FR, en-US, es-AR)
    const prefix = lower.split("-")[0]
    if (prefixMap[prefix]) return prefixMap[prefix]
  }

  // Default to French Quebecois
  return "fr-CA"
}

interface I18nContextType {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: string, variables?: Record<string, string>) => string
}

const I18nContext = createContext<I18nContextType | undefined>(undefined)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("fr-CA")
  const [mounted, setMounted] = useState(false)

  useEffect(() => {
    const detected = detectLocale()
    setLocaleState(detected)
    const langMap: Record<Locale, string> = {
      "fr-CA": "fr",
      "en-CA": "en",
      "es-MX": "es",
    }
    document.documentElement.setAttribute("lang", langMap[detected])
    setMounted(true)
  }, [])

  const setLocale = useCallback((newLocale: Locale) => {
    setLocaleState(newLocale)
    if (typeof window !== "undefined") {
      localStorage.setItem("preferred_locale", newLocale);
    }
    const langMap: Record<Locale, string> = {
      "fr-CA": "fr",
      "en-CA": "en",
      "es-MX": "es",
    }
    document.documentElement.setAttribute("lang", langMap[newLocale])
  }, [])

  const t = useCallback(
    (key: string, variables?: Record<string, string>) => {
      let text = translations[locale]?.[key] ?? key
      if (variables) {
        Object.entries(variables).forEach(([vKey, vValue]) => {
          text = text.replace(`{${vKey}}`, vValue)
        })
      }
      return text
    },
    [locale]
  )

  // Prevent hydration mismatch - render with default locale on server
  if (!mounted) {
    const serverT = (key: string) => translations["fr-CA"]?.[key] ?? key
    return (
      <I18nContext.Provider value={{ locale: "fr-CA", setLocale, t: serverT }}>
        {children}
      </I18nContext.Provider>
    )
  }

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error("useI18n must be used within I18nProvider")
  return ctx
}
